package org.au.tonomy.shared.util;

/**
 * Extra math stuff.
 */
public class ExtraMath {

  /**
   * Bob Palais' TAU = 2 PI.
   */
  public static final double TAU = 2 * Math.PI;

  /**
   * Multiply by this to convert from degrees to radians.
   */
  public static final double DEGREES_TO_RADIANS = TAU / 360;;

}
